			<h2 class="journal-block-title">Abstract</h2>
			<p>Peritonitis is a common complication in patients undergoing continuous ambulatory peritoneal dialysis (CAPD).  It is the most common cause of transferring to hemodialysis. The aim of this study was to determine the risk factor for developing first episode of peritonitis. Patients who have been treated with CAPD for more than 3 months at Hat-Yai hospital were recruited for this study.  Patients who are diagnosed with peritonitis  have two out of these three  manifestations including (1) symptoms such as abdominal pain, fever (2) turbid dialysate effluent with white cell count > 100 /mm3 and PMN> 50% of WBC and (3) identification of dialysate organism. The data was retrospectively reviewed from 2005 to 2011 and analyzed appropriately with either Chiâ€™s square and logistic regression.</p>
